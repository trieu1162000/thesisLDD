void delay (uint32_t us)
{
	__HAL_TIM_SET_COUNTER(&htim1, 0);
	while ((__HAL_TIM_GET_COUNTER(&htim1))<us);
}
volatile uint32_t local_time, sensor_time;
volatile uint32_t distance;

uint32_t hcsr04_read (void)
{
	local_time=0;
	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_7 | GPIO_PIN_1, GPIO_PIN_RESET);  // pull the TRIG pin HIGH
	delay(2);  // wait for 2 us


	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_7 | GPIO_PIN_1, GPIO_PIN_SET);  // pull the TRIG pin HIGH
	delay(10);  // wait for 10 us
	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_7 | GPIO_PIN_1, GPIO_PIN_RESET);  // pull the TRIG pin low

	// read the time for which the pin is high

	while (!(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_8)));  // wait for the ECHO pin to go high
	while (HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_8))    // while the pin is high
	 {
		local_time++;   // measure time for which the pin is high
		delay (1);
	 }
	return local_time;
}

